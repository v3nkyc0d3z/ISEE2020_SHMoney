package com.example.strawhats;

public class ModeItem {
    private String mModeName;
    private int mModeImage;

    public ModeItem(String mModeName, int mModeImage) {
        this.mModeName = mModeName;
        this.mModeImage = mModeImage;
    }

    public String getmModeName() {
        return mModeName;
    }

    public int getmModeImage() {
        return mModeImage;
    }
}
